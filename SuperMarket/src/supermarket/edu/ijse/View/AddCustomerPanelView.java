    private void tableloard() throws ClassNotFoundException, SQLException{
        String [] columns  = {"ID","Title","Name","DOB","Salary","Address","City","Province","Postal Code"};
        DefaultTableModel dtm = new DefaultTableModel(columns, 0);
        
        
        List<CustomerDto> dtos = custController.getAll();
        
        for(CustomerDto dto: dtos){
            Object [] rowdata = {
                dto.getCustID(),
                dto.getCustTitle(),
                dto.getCustName(),
                dto.getDOB(),
                dto.getSalary(),
                dto.getCustAddress(),
                dto.getCity(),
                dto.getProvince(),
                dto.getPostalCode()
            };
            
            dtm.addRow(rowdata);
        }
        
        table.setModel(dtm);
    }
    
    
