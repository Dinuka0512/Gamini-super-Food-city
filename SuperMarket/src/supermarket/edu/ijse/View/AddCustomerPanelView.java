    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        boolean res;
        try {
            res = saveCustomers();
            
            if(res == true){
                clearData();
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomerPanelView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomerPanelView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private boolean saveCustomers() throws ClassNotFoundException, SQLException{
        CustomerDto dto  = new CustomerDto(
                txtId.getText(),
                txtTitle.getText(),
                txtName.getText(),
                txtDob.getText(),
                Integer.parseInt(txtSalary.getText()),
                txtAddress.getText(),
                txtCity.getText(),
                txtProvince.getText(),
                txtPostal.getText()
        );
        
        boolean res = custController.saveCustomer(dto);
        
        if(res == true){
            JOptionPane.showMessageDialog(this, "Save Sucsessfully");
        }else{
            JOptionPane.showMessageDialog(this, "Failed");
        }
        
        return res;
    }
    
